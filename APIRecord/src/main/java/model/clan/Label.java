package model.clan;

public class Label {
	private int id;
	private String name;
	private LabelIconUrl iconUrls;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public LabelIconUrl getIconUrls() {
		return iconUrls;
	}
	public void setIconUrls(LabelIconUrl iconUrls) {
		this.iconUrls = iconUrls;
	}

	
}
