package model.clan;

public class League {
	
	private int id;
	private String name;
	private IconUrl iconUrls;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public IconUrl getIconUrls() {
		return iconUrls;
	}
	public void setIconUrls(IconUrl iconUrls) {
		this.iconUrls = iconUrls;
	}
	

}
